package org.protogalaxy.phss.datasource.entity.filesystem.document;

import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.LastModifiedDate;
import org.springframework.data.annotation.PersistenceConstructor;
import org.springframework.data.mongodb.core.mapping.Document;
import org.springframework.data.mongodb.core.mapping.Field;

import javax.persistence.*;
import java.util.Date;
import java.util.UUID;

@Document(collection = "document")
public class DocumentAdobePdfEntity {
    @Id
    @GeneratedValue
    private UUID uuid;

    @Field("title")
    private String title;

    @Field("created")
    private Date created;

    @Field("modified")
    private Date modified;

    @Field("last_access")
    private Date lastAccess;

    @Field("path")
    private String path;

    @Field("url")
    private String url;

    @Field("version")
    private String version;

    @Field("producer")
    private String producer;

    @Field("gmt_create")
    @Temporal(TemporalType.TIMESTAMP)
    @CreatedDate
    private Date gmtCreate;

    @Field("gmt_modified")
    @Temporal(TemporalType.TIMESTAMP)
    @LastModifiedDate
    private Date gmtModified;

    @PersistenceConstructor
    public DocumentAdobePdfEntity(String title, Date created, Date modified, Date lastAccess, String path, String url, String version, String producer) {
        this.title = title;
        this.created = created;
        this.modified = modified;
        this.lastAccess = lastAccess;
        this.path = path;
        this.url = url;
        this.version = version;
        this.producer = producer;
    }

    public UUID getUuid() {
        return uuid;
    }

    public void setUuid(UUID uuid) {
        this.uuid = uuid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Date getModified() {
        return modified;
    }

    public void setModified(Date modified) {
        this.modified = modified;
    }

    public Date getLastAccess() {
        return lastAccess;
    }

    public void setLastAccess(Date lastAccess) {
        this.lastAccess = lastAccess;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getProducer() {
        return producer;
    }

    public void setProducer(String producer) {
        this.producer = producer;
    }

    public Date getGmtCreate() {
        return gmtCreate;
    }

    public void setGmtCreate(Date gmtCreate) {
        this.gmtCreate = gmtCreate;
    }

    public Date getGmtModified() {
        return gmtModified;
    }

    public void setGmtModified(Date gmtModified) {
        this.gmtModified = gmtModified;
    }
}
